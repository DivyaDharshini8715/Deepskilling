    EXECUTE IMMEDIATE 'DROP TABLE ACCOUNTS';
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE ACCOUNTS (
    ACCOUNT_ID NUMBER PRIMARY KEY,
    CUSTOMER_NAME VARCHAR2(50),
    ACCOUNT_TYPE VARCHAR2(20),
    BALANCE NUMBER(10,2)
);
/

